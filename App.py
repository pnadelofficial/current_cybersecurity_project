import streamlit as st

st.title('Bridging the Theory-Practice Gap in the Cyber Domain')
st.markdown('## Assessing Alignment from Theory Application to Strategy Implementation')

st.markdown('### Research question')
st.markdown('How closely aligned were theory, policy, and strategy implementation during U.S. attempts to secure the cyber domain between 2009 and 2023?')

st.markdown('### Theoretical Foundations')
st.markdown("""
The project is rooted in the theory-practice gap literature (pioneered by
Alexander George, expanded by Philip Zelikow and others), which established a disconnect between
scholars and practitioners of foreign policy and posited that both communities would benefit from a
"smaller gap".
""".strip())

st.markdown('### Research Design')
st.markdown("""
The research design utilizes a structured, focused comparison of four case studies from the
national cybersecurity guidance across three different administrations. Applying Zelikow’s policy
engineering framework, I assess the analytic components of the hierarchy of U.S. security strategies from
the National Security Strategy down to the Defense Department Cyber Strategy. The design begins with a
systematic text analysis of the primary sources and terminates with an assessment of several components of
strategy implementation. The research design relies on a dichotomous variable of alignment or
misalignment to determine whether strategies developed during this period appropriately applied the core
assumptions and causal logic of the substantive theories featured in their language.
""".strip())

st.image("images/Screenshot 2024-03-26 at 8.58.06 AM.png")

st.markdown('### Systematic Text Analysis') 
st.markdown("""
The text analysis employs an
a priori—or theoretically derived—coding
technique. Saldana (2021) refers to
a priori coding as Provisional Coding: “a start list of researcher-
generated codes based on what preparatory investigation suggests might appear in the data before they are
collected and analyzed.” (Saldana, 2016) The International Relations theories examined throughout the
Literature Review serve as categories that were disaggregated to apply two
a priori coding techniques:
Concept Coding for the theoretical Core Assumptions, and In Vivo Coding for the key words and phrases. I
used a Qualitative Data Analysis Software program called NVivo to store, manage, and analyze the coded
texts. Using this
a priori coding technique, I identified key words, phrases, and core assumptions
throughout the primary texts that are associated with various theoretical paradigms, then assessed where
and how those concepts overlapped with Zelikow’s Policy Engineering Framework tasks.
""".strip()) 

st.markdown('### Methodological Goal')
st.markdown("""
The design is structured to develop a descriptive analytic framework that illuminates
what a “bridged” theory-practice gap might look like for cybersecurity. Ultimately, the larger research
agenda seeks to offer a framework that provides greater precision in measuring the existence of a gap for
any policy issue, how such gaps might be managed, and how they might matter.
""".strip())